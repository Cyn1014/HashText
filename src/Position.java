
public class Position {

	private int line;
	private int number;
	public int getLine() {
		return line;
	}
	public void setLine(int line) {
		this.line = line;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public Position(int line,int number){
		this.line=line;
		this.number=number;
	}
	

}
